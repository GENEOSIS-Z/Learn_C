#include <stdio.h>

int main()
{
    int a = 8; //You can use int to store real numbers but thats not recommended
    float b = 8.5;
    char c = 'e';
    int d = 45;
    int e = 45 + 10;
    printf("The value of a is %d \n", a); // \n for line
    printf("The value of b is %f \n", b);
    printf("The value of c is %c \n", c);
    printf("The value of a + d is %d \n", a + d);
    printf("The value of e is %d \n", e);
    return 0;
}